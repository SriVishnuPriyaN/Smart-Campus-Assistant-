export interface Message {
  id: string;
  role: 'user' | 'assistant';
  content: string;
  timestamp: Date;
}

export interface Chat {
  id: string;
  title: string;
  messages: Message[];
  pdfText: string;
  pdfName: string;
  chunks: string[];
  createdAt: Date;
  updatedAt: Date;
}

export interface ChatState {
  chats: Record<string, Chat>;
  currentChatId: string | null;
}
